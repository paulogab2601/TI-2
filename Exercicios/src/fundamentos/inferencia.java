package fundamentos;

public class inferencia {

	public static void main(String[] args) {
		double a = 4.5;

		System.out.println(a);

		var b = 4.5;
		System.out.println(b);

		var c = "Texto\n";
		System.out.println(c);
	}
	// A linguagem descobre o tipo da variavel, mesmo sem declarar, porém esse tipo
	// não pode sofrer alteração ao longo
}
